import type { Player, PlayerChampionProfile, Role } from "../types/game";

type FreeAgentInput = {
  id: string;
  name: string;
  role: Role;
  secondaryRoles?: Role[];
  age: number;
  salaryExpectation: number;
  ability: number;
  potential: number;
  overall: number;
  mechanics: number;
  macro: number;
  laning: number;
  teamfight: number;
  mental: number;
  championPool: number;
  traits: string[];
  championProfile?: Partial<PlayerChampionProfile>;
};

function createChampionProfile(
  input: FreeAgentInput,
): PlayerChampionProfile {
  return {
    preferredChampionIds: input.championProfile?.preferredChampionIds ?? [],
    dislikedChampionIds: input.championProfile?.dislikedChampionIds ?? [],
    signatureChampionIds: input.championProfile?.signatureChampionIds ?? [],
    masteryOverrides: input.championProfile?.masteryOverrides ?? {},
    preferredArchetypes: input.championProfile?.preferredArchetypes ?? input.traits,
  };
}

function createFreeAgent(input: FreeAgentInput): Player {
  return {
    ...input,
    secondaryRoles: input.secondaryRoles ?? [],
    region: "lck",
    league: "LCK",
    availableForRoster: true,
    cost: input.salaryExpectation,
    status: {
      form: 62,
      fatigue: 8,
      morale: "neutral",
      condition: 94,
      injuryRisk: 9,
    },
    mindset: {
      pressureResistance: input.mental,
      clutch: Math.round((input.teamfight + input.mental) / 2),
      consistency: Math.round((input.ability + input.mental) / 2),
      tiltControl: input.mental,
      leadership: input.role === "mid" || input.role === "support" ? 80 : 70,
      teamwork: Math.round((input.macro + input.teamfight) / 2),
      communication: input.role === "jungle" || input.role === "support" ? 80 : 72,
      affinity: 72,
      professionalism: 78,
      ambition: Math.max(68, input.potential - 4),
    },
    adaptability: {
      metaAdaptability: Math.round((input.macro + input.championPool) / 2),
      patchAdaptability: Math.round((input.championPool + input.potential) / 2),
      roleFlexibility: (input.secondaryRoles ?? []).length > 0 ? 74 : 58,
      championLearning: Math.round((input.championPool + input.potential) / 2),
      internationalAdaptability: Math.round((input.mental + input.macro) / 2),
    },
    chemistryProfile: {
      preferredTeammates: [],
      dislikedTeammates: [],
      synergyTags: input.traits.slice(0, 2),
      playstyleTags: input.traits,
      personalityTags: input.role === "support" ? ["connector"] : ["competitive"],
      languageTags: ["Korean"],
    },
    championProfile: createChampionProfile(input),
    development: {
      growthRate: Math.max(35, input.potential - input.ability + 45),
      peakAgeStart: 22,
      peakAgeEnd: 27,
      declineRate: 8,
      prospectTier: input.age <= 19 ? "A" : undefined,
    },
    marketProfile: {
      marketability: Math.round((input.overall + input.mental) / 2),
      fanbase: Math.round((input.overall + input.ability) / 2),
      brandRisk: 10,
    },
  };
}

