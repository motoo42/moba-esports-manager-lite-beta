// Generates src/domain/items/itemGold.ts from a local Riot Data Dragon item.json.
//
// The build/dev environment here has no network egress, so the download is done
// separately (see usage); this script only reads a local file + the item pool and
// emits a static, version-pinned price map. The app never fetches at runtime.
//
// Usage (from repo root):
//   1. Download the matching Data Dragon item.json (network required):
//        curl -o item.json https://ddragon.leagueoflegends.com/cdn/16.12.1/data/en_US/item.json
//      (or PowerShell: Invoke-WebRequest <url> -OutFile item.json -UseBasicParsing)
//   2. node scripts/generate-item-gold.mjs item.json
//   3. delete item.json (it is not committed)
//
// Re-run after changing the item pool or bumping the Data Dragon version.

import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const itemPoolPath = path.join(repoRoot, "src/domain/items/itemPool.ts");
const outputPath = path.join(repoRoot, "src/domain/items/itemGold.ts");
const itemJsonPath = path.resolve(repoRoot, process.argv[2] ?? "item.json");

const dataDragonVersion = "16.12.1";

// Pull every item id out of the pool so the price map tracks it automatically.
const poolSource = readFileSync(itemPoolPath, "utf8");
const poolIds = [...poolSource.matchAll(/id:\s*"(\d+)"/g)].map((match) => match[1]);

if (poolIds.length === 0) {
  throw new Error(`No item ids found in ${itemPoolPath}`);
}

const itemData = JSON.parse(readFileSync(itemJsonPath, "utf8")).data;

const missing = [];
const totals = {};

for (const id of poolIds) {
  const total = itemData[id]?.gold?.total;

  if (typeof total !== "number" || total <= 0) {
    missing.push(id);
    continue;
  }

  totals[id] = total;
}

if (missing.length > 0) {
  throw new Error(
    `Missing/invalid gold.total for item ids: ${missing.join(", ")} ` +
      `(check the item.json version matches ${dataDragonVersion}).`,
  );
}

const entries = Object.keys(totals)
  .sort((first, second) => Number(first) - Number(second))
  .map((id) => `  "${id}": ${totals[id]},`)
  .join("\n");

const output = `// AUTO-GENERATED by scripts/generate-item-gold.mjs — do not edit by hand.
// Source: Riot Data Dragon ${dataDragonVersion} (cdn/${dataDragonVersion}/data/en_US/item.json),
// field gold.total, for every item in the live-match item pool. Re-run the generator
// after changing the pool or bumping the Data Dragon version.

// Total gold cost of each completed item, keyed by Riot item id.
export const itemGoldTotals: Record<string, number> = {
${entries}
};

// Fallback cost for an unknown id — a typical legendary price, so a missing entry
// never collapses the build pacing (used by the proportional purchase model).
export const defaultItemGold = 3000;

export function getItemGoldTotal(itemId: string): number {
  return itemGoldTotals[itemId] ?? defaultItemGold;
}
`;

writeFileSync(outputPath, output, "utf8");
console.log(`Wrote ${Object.keys(totals).length} item prices to ${outputPath}`);
